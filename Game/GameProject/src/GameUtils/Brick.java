package Objects;

/**
 * @author Naheem J.
 * Brick class containing the X value and the Y value
 */
public class Brick {

    private int xValue;

    private int yValue;

    /**
     * Getter for the variable xValue
     * @return xValue integer
     */
    public int getxValue() {
        return xValue;
    }

    /**
     * Setter for xValue variable
     * @param xValue the xValue integer
     */
    public void setxValue(int xValue) {
        this.xValue = xValue;
    }

    /**
     * Getter for yValue variable
     * @return the yValue variable
     */
    public int getyValue() {
        return yValue;
    }

    /**
     * Setter for yValue variable
     * @param yValue the yValue variable
     */
    public void setyValue(int yValue) {
        this.yValue = yValue;
    }
}
